public class Points {
    int xKoordinate;
    int yKoordinate;

    Points(int xKoordinate, int yKoordinate){
        this.xKoordinate = xKoordinate;
        this.yKoordinate = yKoordinate;
    }

    public int getXKoordinate() {
      return xKoordinate;
    }

    public int getYKoordinate() {
        return yKoordinate;
    }
}
